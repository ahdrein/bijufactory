    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        Object opcoes[] = {"Sim","Não"};
        //JDialog.setDefaultLookAndFeelDecorated(true);
        int op = JOptionPane.showOptionDialog(null, "Confirma a exclusão da formula?", "Excluindo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE , null, opcoes, null);
        if(op == JOptionPane.YES_OPTION ){

            ArrayList collectionFormula = new ArrayList();
            collectionFormula = formulaController.getCollectionFormula();
            formulaController.setCollectionFormulaRemoved(collectionFormula);

            formulaController.removerFormula();
            this.limpaComponetes();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

