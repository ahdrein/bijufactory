    private void txSCodigoProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txSCodigoProdutoActionPerformed
        String sCodigoProduto = this.getTxSCodigoProduto().getText();
        this.limpaComponetes();
        this.getTxSCodigoProduto().setText(sCodigoProduto);
        String msg = formulaController.validaProduto(sCodigoProduto);
        if(msg != null && !msg.equals("")){
            JOptionPane.showMessageDialog(rootPane, msg);
        }else{
            this.habilitaComponentes(true);
            this.txSCodigoMateriaPrima.grabFocus();
            ArrayList collectionFormula = new ArrayList();
            collectionFormula = formulaController.validaFormula();
            if (collectionFormula != null){
                Iterator i = collectionFormula.iterator();
                while(i.hasNext()){
                    BjFormula bjFormula = (BjFormula)i.next();
                    String[] row = new String []{bjFormula.getSCodigoMateriaPrima().getSCodigo(),bjFormula.getNQtde().toString()};
                    getModelo().addRow(row);
                }
            }

        }

    }//GEN-LAST:event_txSCodigoProdutoActionPerformed

