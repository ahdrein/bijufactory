    private void limpaComponetes(){
        this.txSCodigoProduto.setText("");
        this.txSCodigoMateriaPrima.setText("");
        this.txQuantidade.setValue(1);
        
        formulaController = new FormulaController();
        dados = new String [][]{
            {"",""}
        };

        //dados = new String [][]{
        //             {"SP","Sao Paulo"},
        //             {"RJ","Rio de Janeiro"},
        //             {"RN","Rio Grande do Norte"},
        //             {"PR","Parana"}
        //};
        colunas = new String []{"Cod. Materia Prima","Quantidade"};
        // Ao inves de passar direto, colocamos os dados em um modelo
        modelo = new DefaultTableModel(colunas, 0);
        // e passamos o modelo para criar a jtable
        tbFormulaProduto.setModel( modelo );

        formulaController.inicializaObjetos();
        this.habilitaComponentes(false);
        this.txSCodigoProduto.grabFocus();
    }

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        if(getTxSCodigoProduto().getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Código do produto não informado!");
        }else{
            String msg = formulaController.validaProduto(getTxSCodigoProduto().getText());
            if(msg != null && !msg.equals("")){
                JOptionPane.showMessageDialog(rootPane, msg);
            }else{
                if(Integer.parseInt(getTxQuantidade().getValue().toString()) < 1){
                    JOptionPane.showMessageDialog(rootPane, "A quantidade não pode ser menor que [ 1 ] !");
                }else{
                    if(getTxSCodigoMateriaPrima().getText().equals("")){
                        JOptionPane.showMessageDialog(rootPane, "Código da matéria prima não informado!");
                    }else{
                        msg = formulaController.validaMateriaPrima(getTxSCodigoMateriaPrima().getText());
                        if(msg != null && !msg.equals("")){
                            JOptionPane.showMessageDialog(rootPane, msg);
                        }else{
                            String[] row = new String []{getTxSCodigoMateriaPrima().getText(),getTxQuantidade().getValue().toString()};
                            getModelo().addRow(row);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        // Obtem a linha selecionada na tabela e chama o método
        // para excluir a linha
        int linhaSelecionada = tbFormulaProduto.getSelectedRow();

        // Verificamos se existe realmente alguma linha selecionada
        if( linhaSelecionada < 0 ){
            return;
        }else{
            // Remove a linha do modelo
            formulaController.addCollectionFormulaRemoved(getModelo().getValueAt(linhaSelecionada, 0).toString());
            formulaController.removerCollectionFormula(getModelo().getValueAt(linhaSelecionada, 0).toString(), getModelo().getValueAt(linhaSelecionada, 1).toString());
            System.out.println(getModelo().getValueAt(linhaSelecionada, 0).toString());
            getModelo().removeRow(linhaSelecionada);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

