    private void limpaComponentes(){
        this.getTxSCodigoFornecedor().setText("");
        this.getTxSCodigoMateriaPrima().setText("");
        this.getTxValorCusto().setValue(0);

        fornecedorMateriaPrimaController = new CotacaoController();
        dados = new String [][]{
            {"",""}
        };

        //dados = new String [][]{
        //             {"SP","Sao Paulo"},
        //             {"RJ","Rio de Janeiro"},
        //             {"RN","Rio Grande do Norte"},
        //             {"PR","Parana"}
        //};
        colunas = new String []{"Cod. Materia Prima","Vlr. Custo"};
        // Ao inves de passar direto, colocamos os dados em um modelo
        modelo = new DefaultTableModel(colunas, 0);
        // e passamos o modelo para criar a jtable
        tableFornecedorMateriaPrima.setModel( getModelo());

        fornecedorMateriaPrimaController.inicializaObjetos();
        this.habilitaComponentes(false);
        this.getTxSCodigoFornecedor().grabFocus();
    }

