    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        // Obtem a linha selecionada na tabela e chama o método
        // para excluir a linha
        int linhaSelecionada = tableFornecedorMateriaPrima.getSelectedRow();

        // Verificamos se existe realmente alguma linha selecionada
        if( linhaSelecionada < 0 ){
            return;
        }else{
            // Remove a linha do modelo
            fornecedorMateriaPrimaController.addCollectionFormulaRemoved(getModelo().getValueAt(linhaSelecionada, 0).toString());
            fornecedorMateriaPrimaController.removerCollectionFormula(getModelo().getValueAt(linhaSelecionada, 0).toString(), getModelo().getValueAt(linhaSelecionada, 1).toString());
            System.out.println(getModelo().getValueAt(linhaSelecionada, 0).toString());
            getModelo().removeRow(linhaSelecionada);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        if(getTxSCodigoFornecedor().getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Código do fornecedor não informado!");
        }else{
            String msg = fornecedorMateriaPrimaController.validaFornecedor(getTxSCodigoFornecedor().getText());
            if(msg != null && !msg.equals("")){
                JOptionPane.showMessageDialog(rootPane, msg);
            }else{
                if(Integer.parseInt(getTxValorCusto().getValue().toString()) < 1){
                    JOptionPane.showMessageDialog(rootPane, "A quantidade não pode ser menor que [ 1 ] !");
                }else{
                    if(this.getTxSCodigoMateriaPrima().getText().equals("")){
                        JOptionPane.showMessageDialog(rootPane, "Código da matéria prima não informado!");
                    }else{
                        msg = fornecedorMateriaPrimaController.validaMateriaPrima(getTxSCodigoMateriaPrima().getText());
                        if(msg != null && !msg.equals("")){
                            JOptionPane.showMessageDialog(rootPane, msg);
                        }else{
                            String[] row = new String []{getTxSCodigoMateriaPrima().getText(),getTxValorCusto().getValue().toString()};
                            getModelo().addRow(row);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btAddActionPerformed

    private void txSCodigoFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txSCodigoFornecedorActionPerformed
        String sCodigoFornecedor = this.getTxSCodigoFornecedor().getText();
        this.limpaComponentes();
        this.getTxSCodigoFornecedor().setText(sCodigoFornecedor);
        String msg = fornecedorMateriaPrimaController.validaFornecedor(sCodigoFornecedor);
        if(msg != null && !msg.equals("")){
            JOptionPane.showMessageDialog(rootPane, msg);
        }else{
            this.habilitaComponentes(true);
            this.getTxSCodigoMateriaPrima().grabFocus();
            ArrayList collectionFornecedorMateriaPrima = new ArrayList();
            collectionFornecedorMateriaPrima = fornecedorMateriaPrimaController.validaFornecedorMateriaPrima();
            if (collectionFornecedorMateriaPrima != null){
                Iterator i = collectionFornecedorMateriaPrima.iterator();
                while(i.hasNext()){
                    BjCotacao bjFornecedorMateriaPrima = (BjCotacao)i.next();
                    String[] row = new String []{bjFornecedorMateriaPrima.getSCodigoMateriaPrima().getSCodigo(),
                                                 bjFornecedorMateriaPrima.getnValorCusto().toString()};
                    getModelo().addRow(row);
                }
            }

        }
    }//GEN-LAST:event_txSCodigoFornecedorActionPerformed

