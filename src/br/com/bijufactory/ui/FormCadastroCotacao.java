    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        Object opcoes[] = {"Sim","Não"};
        //JDialog.setDefaultLookAndFeelDecorated(true);
        int op = JOptionPane.showOptionDialog(null, "Confirma a exclusão da formula?", "Excluindo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE , null, opcoes, null);
        if(op == JOptionPane.YES_OPTION ){

            ArrayList collectionFornecedorMateriPrima = new ArrayList();
            collectionFornecedorMateriPrima = fornecedorMateriaPrimaController.getCollectionFornecedorMateriaPrima();
            fornecedorMateriaPrimaController.setCollectionFornecedorMateriaPrimaRemoved(collectionFornecedorMateriPrima);

            fornecedorMateriaPrimaController.removerFornecedorMateriaPrima();
            this.limpaComponentes();
        }
}//GEN-LAST:event_btExcluirActionPerformed

